
let month = Number(prompt("Enter number"));

switch (month) {
    case 1:
        console.log("January");
        break;
    case 2:
        console.log("february");
    case 3:
        console.log("march");
    case 4:
        console.log("april");
    case 5:
        console.log("may");
    case 6:
        console.log("june");
    case 7:
        console.log("july");
        break
    case 8:
        console.log("August");
        break
    case 9:
        console.log("September");
        break
    case 10:
        console.log("October");
        break
    case 11:
        console.log("November");
        break;
    case 12:
        console.log("December");
        break;
    default:
        console.log("Incorrect");
        

}